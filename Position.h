typedef struct Vector2D
{
	int x;
	int y;
} Vector2D;

typedef struct Vector2Df
{
	float x;
	float y;
} Vector2Df;

typedef struct Vector2Dd
{
	double x;
	double y;
} Vector2Dd;

typedef struct Vector2Du
{
	unsigned char x;
	unsigned char y;
} Vector2Du;

typedef struct Vector3D
{
	int x;
	int y;
	int z;
} Vector3D;

typedef struct Vector3Df
{
	float x;
	float y;
	float z;
} Vector3Df;

typedef struct Vector3Dd
{
	double x;
	double y;
	double z;
} Vector3Dd;

typedef struct Vector3Du
{
	unsigned char x;
	unsigned char y;
	unsigned char z;
} Vector3Du;